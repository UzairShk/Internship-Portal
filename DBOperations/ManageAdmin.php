<?php

    require_once "DBConnection.php";

   class ManageAdmin
   {
       private $DBObj;
       private $Con;
       public function __construct()
       {
          $this->DBObj = new DBConnection();
          $this->Con = $this->DBObj->getConnection();
       }

       public function addData($Model)
       {
        $Query="INSERT INTO `ip`.`adminmaster` (`AdminId`, `FullName`, `ContactNo`, `Email`, `UserName`, `Password`) VALUES 
                                (NULL, '$Model->FullName', '$Model->ContactNo', '$Model->Email', '$Model->UserName', '$Model->Password');";

                if (mysqli_query($this->Con, $Query))
                {
                    echo "Inserted Successfully";
                }
                else
                {
                    echo mysqli_error($this->Con);
                }
       }

       public function updateData($Model)
       {
                $Query="UPDATE `ip`.`adminmaster` SET `FullName`= '$Model->FullName', `ContactNo`='$Model->ContactNo', 
                                    `Email`='$Model->Email', `UserName`= '$Model->UserName', `Password`='$Model->Password'
                                    WHERE `adminmaster`.`AdminId` =$Model->AdminId";

                if (mysqli_query($this->Con, $Query))
                {
                    echo "Updated Successfully";
                }
                else
                {
                    echo mysqli_error($this->Con);
                }
       }

       public function deleteData($Id)
       {
            $Query ="Delete from adminmaster where AdminId=$Id";
           if (mysqli_query($this->Con, $Query))
           {
                return "Deleted Successfully";
           }
           else
           {
              return mysqli_error($this->Con);
           }
       }

       public function viewData()
       {
                    $dataRows = array();
                    $Query = "SELECT * FROM adminmaster";
                    $Result = mysqli_query($this->Con, $Query);

                    while ($row = mysqli_fetch_assoc($Result))
                    {
                        $dataRows[] = $row;
                    }
                        return json_encode($dataRows);
        }

       public function getDetails($Id)
       {
           $Query = "Select * from adminmaster where AdminId=$Id";
           $Result = mysqli_query($this->Con, $Query);
           $Data = mysqli_fetch_assoc($Result);
           return json_encode($Data);
       }
   }
?>